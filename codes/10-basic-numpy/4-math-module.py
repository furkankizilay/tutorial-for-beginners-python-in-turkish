import math
print(math.fabs(-25)) #parantez içindeki değerin mutlak değerini alır.
print(math.ceil(5.1)) #parantez içindeki değeri bir üst tam sayıya dönüştürür.
print(math.floor(5.9)) #parantez içindeki değeri bir alt tam sayıya dönüştürür.
print(math.factorial(5)) #parantez içindeki değerin faktöriyelini hesaplar.
print(math.fmod(5,3)) #5'in 3'e bölümünden kalanı verir.
print(math.frexp(5)) #parantez içindeki değerin kayan nokta eşdeğerini (mantis,us) döndürür. mantis float, us int tipindendir.
print(math.fsum([2.5,2.8,3.2])) #bir kesirli sayı listesinin toplamını verir.
print(math.gcd(12,8)) #ilk ve ikinci değerin obeb'ini alır.
print(math.trunc(-2.5)) #parantez içindeki değerin işareti dikkate alınmadan bir alt tam sayıya dönüştrür.
print(math.copysign(-8,+7)) #ikinci değein işaretini birinci değere kopyalar.
print(math.degrees(1)) #parantez içindeki değeri radyandan dereceye çevirir.
print(math.radians(9)) #parantezin içindeki değeri dereceden radyana dönüştürür.
print(math.cos(0.0)) #x'in radyan cinsinden trigonemetrik cosinüsünü hesaplar.
print(math.sin(0.0)) #x'in radyan cinsinden trigonemetrik sinüsünü hesaplar.
print(math.tan(0.0)) #x'in radyan cinsinden trigonemetrik tanjantını hesaplar.
print(math.acos(0.0)) #x'in radyan cinsinden trigonemetrik cosinüsünün tersini hesaplar.
print(math.asin(0.0)) #x'in radyan cinsinden trigonemetrik sinüsünün tersini hesaplar.
print(math.atan(0.0)) #x'in radyan cinsinden trigonemetrik tanjantının tersini hesaplar.
print(math.sqrt(9)) #parantez içindeki değerin karekökünü verir.
print(math.hypot(1,0)) #birinci ve ikinci değerin karelerinin toplamının karekökünü verir.
print(math.log(2.71828)) #e tabanına göre parantezin içindeki değerin logaritmasını alır.
print(math.exp(1.0)) #doğal logaritma parametresi olan e'nin üstel değerini(e**x) hesaplar.
print(math.log10(20)) #10 tabanına göre parantez içerisindeki değerin logaritması alınır.
print(math.pi)
print(math.e)
print(math.tau) #tau sayısı pi*2'dir.